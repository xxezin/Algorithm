# (세로로 봐보면)
# 1,3,6,10,...
# 2,5,9,14,...
# 4,8,13,...
# 7,12,18,...
# 11,...

# (가로로 봐보면)
# 1,2,4,7,11,...
# 3,5,8,12,...
# 6,9,13,...
# 10,14,19,...
# 15,...

# i+j 인덱스 합만큼 갯수가 있음
# if 1 == > X - i

X = int(input())
i = 0

while X > i+1:
    i+=1
    X -= i


if i %2 == 0: # 아래에서 시작되는거면
    print('{0}/{1}'.format(i+2-X,X))
else: # 위에서 시작되는 거면
    print('{0}/{1}'.format(X,i+2-X))
