n = int(input())
D = [[0]*10 for _ in range(n+1)]

for i in range(1,10):
    D[1][i] = 1

for i in range(2,n+1):
    for j in range(10):
        if j==0:
            D[i][j] = D[i-1][1]
        
        elif 1 <= j <= 8:
            D[i][j] = D[i-1][j-1] + D[i-1][j+1]
        
        else:
            D[i][j] = D[i-1][8]
    
print(sum(D[n])%1000000000)

